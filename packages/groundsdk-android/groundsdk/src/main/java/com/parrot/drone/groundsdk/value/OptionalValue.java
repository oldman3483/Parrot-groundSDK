package com.parrot.drone.groundsdk.value;

/**
 * A container for a value that may be completely unsupported or may not be available at the time of request.
 */
public abstract class OptionalValue {

    /**
     * Tells whether the value is currently available.
     * <p>
     * Value may be completely unsupported depending on the drone model and/or firmware version, or may also be
     * unavailable at the present time. In such cases, this method returns {@code false}
     * </p>
     *
     * @return {@code true} if the value is available, {@code false} otherwise
     */
    public abstract boolean isAvailable();
}
